public class Traffic_light {
    private final int x;
    private boolean isGreen;
    public Traffic_light(int x, boolean color) {
        color = isGreen;
        this.x = x;
    }
    public int getX() {
        return x;
    }
    public boolean isGreen() {
        return isGreen;
    }
    public void setGreen(boolean green) {
        isGreen = green;
    }
}